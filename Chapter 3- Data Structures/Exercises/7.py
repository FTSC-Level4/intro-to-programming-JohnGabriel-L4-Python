# I created a list 'places
places = ['Rome','Paris','Tokyo','Greece','Spain']

#ORIGINAL
print(places,"\n	  ")

#ALPHABETICAL ORDER
print(sorted(places),"\n	  ")

#ORIGINAL
print(places,"\n	  ")

#ALPHABETICAL ORDER BUT REVERSED
print(sorted(places, reverse=True),"\n	  ")

#ORIGINAL
print(places,"\n	  ")

#REVERSE(ORIGINAL)
places.reverse()
print(places,"\n	  ")

#BACK TO ORIGINAL
places.reverse()
print(places,"\n	  ")

#ALPHABETICAL ORDER using sort
places.sort()
print(places)

#ALPHABETICAL ORDER but REVERSED
places.sort()
print(places)