# I assigned a string with leading and trailing whitespaces to the variable 'name'.
name = "\tHenry\n"

print(name)
# I used 'lstrip()' to remove leading whitespaces and printed the result.
print(name.lstrip())
# I used 'rstrip()' to remove trailing whitespaces and printed the result.
print(name.rstrip())
# I used 'strip()' to remove both leading and trailing whitespaces and printed the result.
print(name.strip())